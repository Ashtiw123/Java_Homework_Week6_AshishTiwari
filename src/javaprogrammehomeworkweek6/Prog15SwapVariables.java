package javaprogrammehomeworkweek6;

public class Prog15SwapVariables {
    //Main Method
    public static void main(String[] args) {
        //Variable
        int a, b;
        int temp;
        a = 10;
        b = 5;
        temp = a; // Variable to swap
        a = b; // 5
        b = temp; // 10
        // Print method with swap formula
        System.out.println("a = " + a);
        System.out.println("b = " + b);

    }
}
